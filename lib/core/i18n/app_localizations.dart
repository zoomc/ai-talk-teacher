/// SpeakFlow internationalization.
///
/// Design:
/// - 7 supported locales: zh (default), en, ja, ko, es, fr, pt.
/// - Browser language is auto-detected on web (see [detectBrowserLocale]) and
///   persisted to `user_settings.app_language`. Users can override in Settings.
/// - App-specific strings live in a single in-memory map keyed by [AppLocale].
///   Material/Cupertino built-in strings come from `flutter_localizations`.
/// - Access pattern: `AppLocalizations.of(context).t('key')`.
library;

import 'package:flutter/material.dart';

/// Supported app locales. Order matters for the Settings picker — `zh` first
/// because it's the project default when detection fails.
enum AppLocale {
  zh('zh', '中文', 'Chinese'),
  en('en', 'English', 'English'),
  ja('ja', '日本語', 'Japanese'),
  ko('ko', '한국어', 'Korean'),
  es('es', 'Español', 'Spanish'),
  fr('fr', 'Français', 'French'),
  pt('pt', 'Português', 'Portuguese');

  final String code;
  final String nativeName;
  final String englishName;
  const AppLocale(this.code, this.nativeName, this.englishName);

  /// Map a BCP-47 language tag (e.g. from `navigator.language` or
  /// `PlatformDispatcher.locale`) to an [AppLocale]. Falls back to [zh]
  /// when nothing matches — per the spec "如果检测不到就是默认中文".
  static AppLocale fromString(String? s) {
    if (s == null || s.isEmpty) return AppLocale.zh;
    final n = s.toLowerCase();
    if (n.startsWith('zh')) return AppLocale.zh;
    if (n.startsWith('ja')) return AppLocale.ja;
    if (n.startsWith('ko')) return AppLocale.ko;
    if (n.startsWith('en')) return AppLocale.en;
    if (n.startsWith('es')) return AppLocale.es;
    if (n.startsWith('fr')) return AppLocale.fr;
    if (n.startsWith('pt')) return AppLocale.pt;
    return AppLocale.zh;
  }

  Locale toLocale() => Locale(code);
}

/// All app strings. Keys are stable identifiers (snake_case). Missing keys
/// fall back to zh, then to the key itself, so a missing translation never
/// crashes — it just shows the key.
class AppLocalizations {
  final AppLocale locale;
  AppLocalizations(this.locale);

  static const LocalizationsDelegate<AppLocalizations> delegate =
      _AppLocalizationsDelegate();

  /// Locales we ship translations + accept in MaterialApp.
  static const List<Locale> supportedLocales = [
    Locale('zh'),
    Locale('en'),
    Locale('ja'),
    Locale('ko'),
    Locale('es'),
    Locale('fr'),
    Locale('pt'),
  ];

  /// Look up [key] in the current locale, falling back to zh, then to [key].
  String t(String key) {
    final map = _translations[locale] ?? _translations[AppLocale.zh]!;
    return map[key] ?? _translations[AppLocale.zh]![key] ?? key;
  }

  /// Convenience for parameterised strings: `tArg('welcome', {'name': 'Sam'})`
  /// replaces `{name}` in the template.
  String tArg(String key, Map<String, String> args) {
    var s = t(key);
    args.forEach((k, v) => s = s.replaceAll('{$k}', v));
    return s;
  }

  static AppLocalizations? maybeOf(BuildContext context) {
    return Localizations.of<AppLocalizations>(context, AppLocalizations);
  }

  static AppLocalizations of(BuildContext context) {
    return maybeOf(context) ?? AppLocalizations(AppLocale.zh);
  }
}

class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    // Every locale resolves to an AppLocale (zh fallback), so this delegate
    // claims to support anything — MaterialApp will still only offer
    // `supportedLocales` to the resolution algorithm.
    return true;
  }

  @override
  Future<AppLocalizations> load(Locale locale) async {
    return AppLocalizations(AppLocale.fromString(locale.languageCode));
  }

  @override
  bool shouldReload(_AppLocalizationsDelegate old) => false;
}

